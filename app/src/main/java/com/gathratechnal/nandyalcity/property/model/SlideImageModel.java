public class SlideImageModel {
    private String id;
    private String slide_title;
    private String slide_trnid;
    private String name;
    private String mobile;
    private String email;
    private String app_url;
    private String app_image;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSlide_title() {
        return slide_title;
    }

    public void setSlide_title(String slide_title) {
        this.slide_title = slide_title;
    }

    public String getSlide_trnid() {
        return slide_trnid;
    }

    public void setSlide_trnid(String slide_trnid) {
        this.slide_trnid = slide_trnid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getApp_url() {
        return app_url;
    }

    public void setApp_url(String app_url) {
        this.app_url = app_url;
    }

    public String getApp_image() {
        return app_image;
    }

    public void setApp_image(String app_image) {
        this.app_image = app_image;
    }

}
